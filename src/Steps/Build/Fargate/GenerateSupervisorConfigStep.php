<?php

namespace Codinglabs\Yolo\Steps\Build\Fargate;

use Codinglabs\Yolo\Paths;
use Codinglabs\Yolo\Manifest;
use Codinglabs\Yolo\Contracts\Step;
use Codinglabs\Yolo\ShutdownTimings;
use Codinglabs\Yolo\Enums\StepResult;
use Illuminate\Filesystem\Filesystem;

/**
 * Generates the container's supervisord.conf into the build context from the
 * manifest — the same way GenerateEntrypointScriptStep generates the entrypoint,
 * so the running processes can't drift from the manifest and the octane port
 * always matches tasks.web.port (the same value the target group health-checks).
 *
 * Octane (the web server) always runs; the scheduler and queue worker run only
 * when tasks.web.scheduler / tasks.web.queue are explicitly true. The code
 * default is false (octane only) — `yolo init` ships a manifest that turns both
 * on, so a fresh app gets the full web task while the absence of config stays
 * conservative. Splitting these into independent queue/scheduler services is a
 * later concern; this step only models the single-container web task.
 */
class GenerateSupervisorConfigStep implements Step
{
    public function __construct(
        protected string $environment,
        protected $filesystem = new Filesystem()
    ) {}

    public function __invoke(): StepResult
    {
        $graces = ShutdownTimings::programGraces();

        $path = Paths::build('docker/supervisord.conf');
        $this->filesystem->ensureDirectoryExists(dirname($path));
        $this->filesystem->put($path, $this->config($graces));

        // The scheduler runs as cron, not a schedule:work daemon, so the per-minute
        // trigger lives in a crontab crond reads — written only when enabled.
        if (isset($graces['scheduler'])) {
            $this->writeCrontab();
        }

        return StepResult::SUCCESS;
    }

    /**
     * @param  array<string, int>  $graces
     */
    protected function config(array $graces): string
    {
        // Stop waits come from ShutdownTimings so a program's graceful-stop window
        // matches the container stopTimeout derived from the same source.
        $blocks = [
            $this->header(),
            $this->program('octane', sprintf(
                'php artisan octane:frankenphp --host=0.0.0.0 --port=%d',
                (int) Manifest::get('tasks.web.port', 8000),
            ), stopwaitsecs: $graces['octane']),
        ];

        if (isset($graces['scheduler'])) {
            // Cron (crond) fires an ephemeral schedule:run each minute rather than a
            // long-lived schedule:work daemon — so the trigger halts cleanly on
            // shutdown and only the in-flight run is waited out (see the entrypoint).
            $blocks[] = $this->program('scheduler', 'crond -f -d 8 -c /app/docker/crontabs', stopwaitsecs: $graces['scheduler']);
        }

        if (isset($graces['queue'])) {
            // Longer stop wait so an in-flight job can finish on SIGTERM before
            // supervisor force-kills the worker.
            $blocks[] = $this->program('queue', 'php artisan queue:work --tries=3 --max-time=3600', stopwaitsecs: $graces['queue']);
        }

        return implode("\n\n", $blocks) . "\n";
    }

    protected function writeCrontab(): void
    {
        $path = Paths::build('docker/crontabs/www-data');

        $this->filesystem->ensureDirectoryExists(dirname($path));
        $this->filesystem->put(
            $path,
            "# Auto-generated by YOLO. Fires the Laravel scheduler once a minute.\n"
            . "* * * * * cd /app && PATH=/usr/local/bin:/usr/bin:/bin php artisan schedule:run >> /proc/1/fd/1 2>&1\n"
        );
    }

    protected function header(): string
    {
        return implode("\n", [
            '# Auto-generated by YOLO from tasks.web.* in yolo.yml — do not edit.',
            '[supervisord]',
            'nodaemon=true',
            'user=www-data',
            'pidfile=/tmp/supervisord.pid',
            'logfile=/dev/null',
            'logfile_maxbytes=0',
            '',
            '[unix_http_server]',
            'file=/tmp/supervisor.sock',
            '',
            '[supervisorctl]',
            'serverurl=unix:///tmp/supervisor.sock',
            '',
            '[rpcinterface:supervisor]',
            'supervisor.rpcinterface_factory = supervisor.rpcinterface:make_main_rpcinterface',
        ]);
    }

    protected function program(string $name, string $command, int $stopwaitsecs = 10): string
    {
        return implode("\n", [
            "[program:$name]",
            "command=$command",
            'autostart=true',
            'autorestart=true',
            "stopwaitsecs=$stopwaitsecs",
            'stdout_logfile=/dev/fd/1',
            'stdout_logfile_maxbytes=0',
            'stderr_logfile=/dev/fd/2',
            'stderr_logfile_maxbytes=0',
        ]);
    }
}
