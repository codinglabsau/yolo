<?php

namespace Codinglabs\Yolo\Steps\Build\Fargate;

use Codinglabs\Yolo\Paths;
use Codinglabs\Yolo\Manifest;
use Codinglabs\Yolo\Contracts\Step;
use Codinglabs\Yolo\ShutdownTimings;
use Codinglabs\Yolo\Enums\StepResult;
use Illuminate\Filesystem\Filesystem;

class GenerateEntrypointScriptStep implements Step
{
    public function __construct(
        protected string $environment,
        protected $filesystem = new Filesystem()
    ) {}

    public function __invoke(): StepResult
    {
        $deployAll = Manifest::get('deploy-all', []);
        $drain = ShutdownTimings::drain();
        $graces = ShutdownTimings::programGraces();

        $body = "#!/bin/sh\n"
            . "# Auto-generated by YOLO. Runs deploy-all: hooks, then supervises the CMD\n"
            . "# with a lame-duck drain so the ALB stops routing before the server stops.\n"
            . "set -e\n\n";

        foreach ($deployAll as $command) {
            $body .= $command . "\n";
        }

        // Past startup. ECS sends SIGTERM at the same moment it deregisters the task,
        // but the ALB takes a few seconds to actually stop routing to a draining
        // target. Backgrounding the CMD lets us trap that SIGTERM and keep serving for
        // the web shutdown-grace-period window before forwarding the stop, so requests the ALB
        // sends mid-drain still land on a live server instead of 502ing. A headless
        // app has no target group to drain, so the sleep is dropped and we stop at once.
        $drainBody = $this->drainBody($drain, $graces);

        $body .= sprintf(<<<'SH'

set +e

draining=0
drain() {
    draining=1
%s    kill -TERM "$child" 2>/dev/null
}
trap drain TERM

"$@" &
child=$!
wait "$child"
status=$?

# wait returns the moment the trap fires; wait again so the child shuts
# down cleanly after we forward the stop.
if [ "$draining" -eq 1 ]; then
    wait "$child"
    status=$?
fi

exit "$status"

SH, $drainBody);

        $path = Paths::build('.yolo-entrypoint.sh');

        $this->filesystem->ensureDirectoryExists(dirname($path));
        $this->filesystem->put($path, $body);
        chmod($path, 0755);

        return StepResult::SUCCESS;
    }

    /**
     * The body of the trap that runs on SIGTERM, before the stop is forwarded to
     * supervisord. With a scheduler it halts cron (no new schedule:run) and waits
     * out any in-flight run; otherwise it's just the lame-duck sleep for the ALB.
     *
     * @param  array<string, int>  $graces
     */
    protected function drainBody(int $drain, array $graces): string
    {
        if (! isset($graces['scheduler'])) {
            return $drain > 0 ? "    sleep $drain\n" : '';
        }

        // Stop cron first so no new schedule:run fires during the drain, then hold
        // the container open until the drain window has elapsed *and* any in-flight
        // schedule:run has finished — bounded by the scheduler's grace so a long job
        // can't stall the deploy past the stopTimeout. A scheduled command is never
        // cut mid-run; one that outlasts the window belongs on the queue instead.
        return sprintf(<<<'SH'
    supervisorctl -c /etc/supervisord.conf stop scheduler >/dev/null 2>&1
    waited=0
    while [ "$waited" -lt %d ]; do
        if [ "$waited" -ge %d ] && ! pgrep -f 'artisan schedule:run' >/dev/null 2>&1; then
            break
        fi
        sleep 1
        waited=$((waited + 1))
    done

SH, max($drain, $graces['scheduler']), $drain);
    }
}
